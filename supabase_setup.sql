-- 10. Contact messages table (for the Contact Us form)
-- ============================================================
CREATE TABLE IF NOT EXISTS public.contact_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    first_name TEXT,
    last_name TEXT,
    email TEXT,
    phone TEXT,
    subject TEXT,
    message TEXT
);

ALTER TABLE public.contact_messages ENABLE ROW LEVEL SECURITY;

-- Allow anyone to insert (contact form is public)
CREATE POLICY "Allow public insert" ON public.contact_messages
    FOR INSERT TO anon, authenticated WITH CHECK (true);

-- Allow admin (anon key) to read and delete
CREATE POLICY "Allow public read" ON public.contact_messages
    FOR SELECT TO anon, authenticated USING (true);

CREATE POLICY "Allow public delete" ON public.contact_messages
    FOR DELETE TO anon, authenticated USING (true);

GRANT ALL ON public.contact_messages TO anon, authenticated, service_role;
